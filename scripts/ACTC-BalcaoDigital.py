            files_to_process_count=files_to_process_count,
            files_parsed_count=files_parsed_count,
            row_level_check_executed=row_level_check_executed,
            balcao_digital_rows_checked=balcao_digital_rows_checked,
            balcao_digital_rows_inserted=balcao_digital_rows_inserted,
