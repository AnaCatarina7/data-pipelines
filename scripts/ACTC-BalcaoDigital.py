# append execution summary rows into the same notification table generated by clts
# ================================================================================
def inject_summary_into_existing_notification_table(toem, summary_rows_html):
    try:
        lower_toem = toem.lower()
        tbody_close_idx = lower_toem.rfind("</tbody>")
        table_close_idx = lower_toem.rfind("</table>")


        if tbody_close_idx != -1:
            return toem[:tbody_close_idx] + summary_rows_html + toem[tbody_close_idx:]
        elif table_close_idx != -1:
            return toem[:table_close_idx] + summary_rows_html + toem[table_close_idx:]
        else:
            return toem + summary_rows_html
    except Exception:
        return toem + summary_rows_html



