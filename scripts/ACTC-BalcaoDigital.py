# helper: parse one file once and classify it as type A or B
# ================================================================================
def parse_balcao_file(filename, buffer):
    cpe = filename.split("_")[1]


    file_type = None
    skip = None
    cols = []


    # auto-detect skiprows and file type
    for sr in [8, 9]:
        buffer.seek(0)
        df_peek = pd.read_excel(buffer, skiprows=sr, header=0, nrows=0)
        cols = df_peek.columns.tolist()
        colmap = build_normalized_column_map(cols)


        data_col = find_first_exact_column(colmap, {"data"})
        hora_col = find_first_exact_column(colmap, {"hora"})


        consumo_col = find_first_column_contains(colmap, [
            "consumo registado"
        ])


        potencia_ativa_col = find_first_column_contains(colmap, [
            "potencia ativa saldo",
            "potencia ativa"
        ])


        if data_col and hora_col and consumo_col:
            file_type = "B"
            skip = sr
            break
        elif data_col and hora_col and potencia_ativa_col:
            file_type = "A"
            skip = sr
            break


    if file_type is None:
        raise ValueError(f"Unknown file type — columns: {cols}")


    buffer.seek(0)
    df = pd.read_excel(buffer, skiprows=skip, header=0)


    raw_cols = df.columns.tolist()
    colmap = build_normalized_column_map(raw_cols)


    data_col = find_first_exact_column(colmap, {"data"})
    hora_col = find_first_exact_column(colmap, {"hora"})


    if not data_col or not hora_col:
        raise ValueError(f"Missing required Data/Hora columns — columns: {raw_cols}")


    df = df.dropna(subset=[data_col, hora_col])
    df["tstamp"] = pd.to_datetime(
        df[data_col].astype(str) + " " + df[hora_col].astype(str),
        format="%Y/%m/%d %H:%M",
        errors="coerce"
    )
    df = df.dropna(subset=["tstamp"])
    df["cpe"] = cpe
    df["id"] = cpe + "_" + df["tstamp"].dt.strftime("%Y%m%d%H%M")


    if file_type == "B":
        estado_col = find_first_exact_column(colmap, {"estado"})
        consumo_col = find_first_column_contains(colmap, [
            "consumo registado"
        ])


        if consumo_col is None:
            raise ValueError(f"Missing consumo column — columns: {raw_cols}")


        df["consumo_kw"] = pd.to_numeric(df[consumo_col], errors="coerce")


        if estado_col is not None:
            df["estado"] = df[estado_col]
        else:
            df["estado"] = None


        df = df[["id", "tstamp", "cpe", "consumo_kw", "estado"]]


    else:
        potencia_ativa_col = find_first_column_contains(colmap, [
            "potencia ativa saldo",
            "potencia ativa"
        ])
        potencia_reativa_ind_col = find_first_column_contains(colmap, [
            "potencia reativa indutiva"
        ])
        potencia_reativa_cap_col = find_first_column_contains(colmap, [
            "potencia reativa capacitiva"
        ])


        if potencia_ativa_col is None:
            raise ValueError(f"Missing potencia ativa column — columns: {raw_cols}")


        df["potencia_ativa"] = pd.to_numeric(df[potencia_ativa_col], errors="coerce")


        if potencia_reativa_ind_col is not None:
            df["potencia_reativa_ind"] = pd.to_numeric(df[potencia_reativa_ind_col], errors="coerce")
        else:
            df["potencia_reativa_ind"] = None


        if potencia_reativa_cap_col is not None:
            df["potencia_reativa_cap"] = pd.to_numeric(df[potencia_reativa_cap_col], errors="coerce")
        else:
            df["potencia_reativa_cap"] = None


        df = df[["id", "tstamp", "cpe", "potencia_ativa", "potencia_reativa_ind", "potencia_reativa_cap"]]


    return file_type, df, cpe



