# fetch existing IDs from CrateDB / TiDB in batches to avoid statement_max_length limits
# ================================================================================
def fetch_existing_ids_in_batches(cursor, table, all_ids, batch_size=500):
    existing = set()


    if not all_ids:
        return existing


    is_tidb = cursor.__class__.__module__.lower().startswith("pymysql")


    for i in range(0, len(all_ids), batch_size):
        batch = all_ids[i:i + batch_size]
        placeholders = ", ".join(["%s" if is_tidb else "?" for _ in batch])
        sql = f"SELECT id FROM {table} WHERE id IN ({placeholders})"
        cursor.execute(sql, tuple(batch))
        existing.update(row[0] for row in cursor.fetchall())


    return existing



