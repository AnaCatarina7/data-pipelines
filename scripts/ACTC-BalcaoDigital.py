# pipeline
# ================================================================================
def run_pipeline(run_params=None):
    global env, hostname, ip, user, script, channel, destination, verbose, send_mail, email_addresses, context


    env = detect_environment()
    print("Running in:", env)


    if env == "colab":
        from google.colab import userdata  # type: ignore
        import ipynbname  # type: ignore
    elif env == "local":
        from dotenv import load_dotenv
        load_dotenv()


    tstart = clts.getts()
    clts.elapt.clear()


    DEFAULT_PARAMS = {
        "verbose": True,
        "destination": "dblist",
        "send_mail": True,
        "email_addresses": ["acatarinatc@gmail.com"],
    }


    if run_params:
        if run_params.get("start_date"):
            DEFAULT_PARAMS["start_date"] = run_params["start_date"]
        if run_params.get("days_back") is not None:
            DEFAULT_PARAMS["days_back"] = int(run_params["days_back"])


    hostname = socket.gethostname()
    ip = requests.get("https://api.ipify.org").text
    print("Server name:", hostname, "Public IP Address:", ip)


    if env == "colab":
        notebookname = requests.get("http://172.28.0.12:9000/api/sessions").json()[0]["name"]
        user = notebookname.split("-")[0]
        script = ipynbname.name()
    else:
        user = os.getenv("USER", "ACTC")
        script = os.path.basename(__file__)


    channel = "balcao_digital"
    destination = DEFAULT_PARAMS["destination"]
    verbose = DEFAULT_PARAMS["verbose"]
    send_mail = DEFAULT_PARAMS["send_mail"]
    email_addresses = DEFAULT_PARAMS["email_addresses"]


    context = f"{hostname} ({ip}) | {user} | {channel} | {script} | {destination}"
    clts.setcontext(context)
    if verbose:
        print("context:", context)
    clts.elapt[f"Environment detected: {env}"] = clts.deltat(tstart)


    # ================================================================================
    # derive date window from run_params
    # ================================================================================
    start_date_str = DEFAULT_PARAMS.get("start_date", datetime.date.today().strftime("%Y-%m-%d"))
    days_back = DEFAULT_PARAMS.get("days_back", 0)
    start_date = datetime.datetime.strptime(str(start_date_str), "%Y-%m-%d").date()
    window_start = start_date - datetime.timedelta(days=days_back)
    window_end = start_date
    print(f"Date window: {window_start} → {window_end}")


    # ================================================================================
    # connect to google drive and list files in folder
    # ================================================================================
    FOLDER_ID = os.getenv("BALCAO_SHARED_FOLDER_ID")


    creds_raw = get_secret_json("ACTC-DriveCredentials.json")
    credentials = service_account.Credentials.from_service_account_info(
        creds_raw,
        scopes=["https://www.googleapis.com/auth/drive.readonly"]
    )
    service = build("drive", "v3", credentials=credentials)
    print("Connected to Google Drive.")


    # list all files in folder (excluding trashed), supporting shared drives
    response = service.files().list(
        q=f"'{FOLDER_ID}' in parents and trashed = false",
        fields="files(id, name, size, modifiedTime)",
        supportsAllDrives=True,
        includeItemsFromAllDrives=True
    ).execute()


    all_files = response.get("files", [])
    clts.elapt["Balcão Digital files list retrieved from Google Drive"] = clts.deltat(tstart)
    print(f"Total files found in folder: {len(all_files)}")


    # ── filter files by date window using filename: Consumos_<CPE>_<YYYYMMDD>HHMMSS.xlsx ──
    files = []
    for f in all_files:
        try:
            date_str = f["name"].split("_")[2][:8]
            file_date = datetime.datetime.strptime(date_str, "%Y%m%d").date()
            if window_start <= file_date <= window_end:
                files.append(f)
        except Exception:
            pass


    clts.elapt[f"Files identified ({window_start} → {window_end}, {days_back} days back)"] = clts.deltat(tstart)
    print(f"Files in window ({window_start} → {window_end}): {len(files)} / {len(all_files)}")
    for f in files:
        print(f"  {f['name']}  |  size: {f.get('size', 'N/A')} bytes  |  modified: {f['modifiedTime']}")


    # ================================================================================
    # download filtered files into memory as BytesIO objects
    # ================================================================================
    raw_files = {}


    for f in files:
        file_id = f["id"]
        filename = f["name"]


        request = service.files().get_media(fileId=file_id)
        buffer = io.BytesIO()
        downloader = MediaIoBaseDownload(buffer, request)
        done = False
        while not done:
            _, done = downloader.next_chunk()


        buffer.seek(0)
        raw_files[filename] = buffer
        print(f"Downloaded into memory: {filename}")


    print(f"\nTotal files in memory: {len(raw_files)}")
    clts.elapt[f"Files downloaded: {len(raw_files)}"] = clts.deltat(tstart)


    if not raw_files:
        print("No files found for the selected date window. Nothing to process.")
        return


    # ================================================================================
    # load dblist - pipeline writes to every database listed here
    # ================================================================================
    dblist = get_secret_json(f"{user}-dblist.json")
    dblist = [db for db in dblist if db in ("crate", "tidb")]
    print("dblist:", dblist)


    # ================================================================================
    # execution summary counters
    # ================================================================================
    files_found_count = len(files)
    files_downloaded_count = len(raw_files)
    files_skipped_by_hash_count = 0
    files_to_process_count = 0
    files_parsed_count = 0


    balcao_digital_rows_checked = None
    balcao_digital_rows_inserted = 0
    balcao_digital_rows_skipped = 0
    balcao_consumo_rows_checked = None
    balcao_consumo_rows_inserted = 0
    balcao_consumo_rows_skipped = 0


    row_level_check_executed = False


    # ================================================================================
    # file-level audit check
    # the goal is to process files that are still missing in at least one database
    # ================================================================================
    files_to_process_global = {}
    files_to_process_by_db = {db: set() for db in dblist}
    file_audit_rows = {}
    skipped_by_hash_by_db = {db: [] for db in dblist}


    for db in dblist:
        print(f"\n--- FILE AUDIT CHECK START [{db}] ---")


        connection = None
        cursor = None
        db_engine = None


        try:
            clts.elapt[f"Connecting to database [{db}]"] = clts.deltat(tstart)
            connection, cursor, db_engine, dbcreds = open_database_connection(user, db)
            clts.elapt[f"... connected to {db}"] = clts.deltat(tstart)


            for filename, buffer in raw_files.items():
                try:
                    buffer.seek(0)
                    file_bytes = buffer.getvalue()


                    audit_row = prepare_file_audit(
                        filename=filename,
                        source="google_drive",
                        file_bytes=file_bytes
                    )


                    print(f"[AUDIT][{db}] filename={filename}")
                    print(f"[AUDIT][{db}] hash={audit_row['file_hash']}")


                    already_processed = file_hash_exists(connection, audit_row["file_hash"])


                    if already_processed:
                        print(f"[AUDIT][{db}] SKIPPED already processed file: {filename}")
                        skipped_by_hash_by_db[db].append(filename)
                        continue


                    print(f"[AUDIT][{db}] NEW file, will continue to parsing: {filename}")
                    files_to_process_by_db[db].add(filename)


                    # keep file globally if it is new in at least one database
                    if filename not in files_to_process_global:
                        file_audit_rows[filename] = audit_row
                        files_to_process_global[filename] = io.BytesIO(file_bytes)


                except Exception as e:
                    print(f"[AUDIT][{db}] Error preparing audit for {filename}: {e}")
                    clts.elapt[f"Error preparing audit for {filename} [{db}]: {e}"] = clts.deltat(tstart)


        except Exception as e:
            print(f"Error connecting to {db}: {e}")
            clts.elapt[f"... error connecting [{db}]: {e}"] = clts.deltat(tstart)


        finally:
            print(f"Connection closing.... [{db}]")
            if connection is not None:
                connection.close()
                clts.elapt[f"... database connection closed [{db}]"] = clts.deltat(tstart)


        print(f"--- FILE AUDIT CHECK END [{db}] ---")


    # files skipped by hash only if skipped in all databases
    if dblist:
        skipped_in_all = set(raw_files.keys())
        for db in dblist:
            skipped_in_all = skipped_in_all.intersection(set(skipped_by_hash_by_db.get(db, [])))
        files_skipped_by_hash_count = len(skipped_in_all)
    else:
        files_skipped_by_hash_count = 0


    files_to_process_count = len(files_to_process_global)


    print("\n--- FILE AUDIT GLOBAL SUMMARY ---")
    print(f"Files skipped by hash in all databases: {files_skipped_by_hash_count}")
    print(f"Files to process after file-level audit: {files_to_process_count} / {len(raw_files)}")
    for db in dblist:
        clts.elapt[f"Files skipped by hash [{db}]"] = clts.deltat(tstart)
        clts.elapt[f"Files pending for [{db}]"] = clts.deltat(tstart)


    # ================================================================================
    # parse files once — detect type A or B by column content
    # Type A: potência (Potência Ativa / Reativa)   → balcao_digital
    # Type B: consumo registado (kW)                → balcao_digital_consumo
    # skiprows is auto-detected (tries 8 then 9)
    # CPE is extracted from the filename: Consumos_<CPE>_<timestamp>.xlsx
    # ================================================================================
    parsed_files_map = {}
    skipped_files = []
    processed_files = []


    if files_to_process_global:
        row_level_check_executed = True


        for filename, buffer in files_to_process_global.items():
            try:
                file_type, df, cpe = parse_balcao_file(filename, buffer)
                parsed_files_map[filename] = {
                    "file_type": file_type,
                    "df": df,
                    "cpe": cpe,
                }
                processed_files.append(filename)
                files_parsed_count += 1


                clts.elapt[f"{filename} parsed (Type {file_type}): {len(df)} records"] = clts.deltat(tstart)
                print(f"{filename}: {len(df)} rows | Type {file_type} | CPE: {cpe}")


            except Exception as e:
                print(f"Error parsing {filename}: {e}")
                skipped_files.append(filename)
                clts.elapt[f"Error parsing {filename}: {e}"] = clts.deltat(tstart)


    clts.elapt["All files parsed"] = clts.deltat(tstart)


    total_a = sum(len(item["df"]) for item in parsed_files_map.values() if item["file_type"] == "A")
    total_b = sum(len(item["df"]) for item in parsed_files_map.values() if item["file_type"] == "B")


    print(f"\nType A (potência):  {total_a:,} rows" if total_a else "Type A: 0 rows")
    print(f"Type B (consumo):   {total_b:,} rows" if total_b else "Type B: 0 rows")
    if skipped_files:
        print(f"Skipped files ({len(skipped_files)}): {skipped_files}")


    # ================================================================================
    # insert into every database in dblist
    # id = cpe + tstamp (YYYYMMDDHHMM, no seconds) — consistent with historical data
    # batching avoids statement_max_length limits
    # ================================================================================
    for db in dblist:
        print(f"\n================ INSERTING INTO [{db}] ================")


        connection = None
        cursor = None
        db_engine = None


        try:
            clts.elapt[f"Connecting to database [{db}]"] = clts.deltat(tstart)
            connection, cursor, db_engine, dbcreds = open_database_connection(user, db)
            clts.elapt[f"... connected to {db}"] = clts.deltat(tstart)


            db_files = [f for f in processed_files if f in files_to_process_by_db[db]]
            print(f"Files to insert into [{db}]: {len(db_files)}")


            df_db_a_list = [parsed_files_map[f]["df"] for f in db_files if parsed_files_map[f]["file_type"] == "A"]
            df_db_b_list = [parsed_files_map[f]["df"] for f in db_files if parsed_files_map[f]["file_type"] == "B"]


            df_db_a = pd.concat(df_db_a_list, ignore_index=True) if df_db_a_list else pd.DataFrame()
            df_db_b = pd.concat(df_db_b_list, ignore_index=True) if df_db_b_list else pd.DataFrame()


            if not df_db_a.empty:
                before_a = len(df_db_a)
                df_db_a = df_db_a.drop_duplicates(subset=["id"], keep="first").copy()
                print(f"[DEDUP][{db}] balcao_digital: {before_a} -> {len(df_db_a)}")


            if not df_db_b.empty:
                before_b = len(df_db_b)
                df_db_b = df_db_b.drop_duplicates(subset=["id"], keep="first").copy()
                print(f"[DEDUP][{db}] balcao_digital_consumo: {before_b} -> {len(df_db_b)}")


            for table, df_insert in [
                ("balcao_digital", df_db_a),
                ("balcao_digital_consumo", df_db_b)
            ]:
                if df_insert.empty:
                    print(f"No data for {table} [{db}], skipping.")
                    continue


                clts.elapt[f"Processing {table} [{db}]"] = clts.deltat(tstart)


                all_ids = df_insert["id"].tolist()
                existing_ids = fetch_existing_ids_in_batches(cursor, table, all_ids)
                clts.elapt[f"... {len(existing_ids)} existing records fetched from {table} [{db}]"] = clts.deltat(tstart)


                values_to_insert, skipped, skipped_list = build_values_to_insert(
                    table=table,
                    df_insert=df_insert,
                    existing_ids=existing_ids
                )
                inserts = len(values_to_insert)


                if values_to_insert:
                    sql = get_insert_sql(table, db_engine)
                    cursor.executemany(sql, values_to_insert)
                    connection.commit()


                    # TIDB: REFRESH TABLE only exists in CrateDB
                    if db_engine == "crate":
                        cursor.execute(f"REFRESH TABLE {table}")


                # keep summary like before (using latest successful run values)
                if table == "balcao_digital":
                    balcao_digital_rows_checked = len(df_insert)
                    balcao_digital_rows_inserted = inserts
                    balcao_digital_rows_skipped = skipped
                else:
                    balcao_consumo_rows_checked = len(df_insert)
                    balcao_consumo_rows_inserted = inserts
                    balcao_consumo_rows_skipped = skipped


                clts.elapt[f"... {inserts} inserted, {skipped} skipped @ {table} [{db}]"] = clts.deltat(tstart)
                print(f"... {inserts} inserted, {skipped} skipped @ {table} [{db}]")
                if skipped_list:
                    print(
                        f"... skipped timestamps @ {table} [{db}]: " + ", ".join(skipped_list[:10]) +
                        (f" ... +{len(skipped_list)-10} more" if len(skipped_list) > 10 else "")
                    )


            # ================================================================================
            # insert processed files into file_audit after successful processing
            # only for files that were actually pending in this database
            # ================================================================================
            for filename in db_files:
                try:
                    audit_row = file_audit_rows.get(filename)
                    if audit_row:
                        insert_file_audit_row(connection, audit_row)
                        print(f"[AUDIT][{db}] inserted into file_audit: {filename}")
                        clts.elapt[f"Audit inserted for {filename} [{db}]"] = clts.deltat(tstart)
                except Exception as e:
                    print(f"Error inserting audit row for {filename} [{db}]: {e}")
                    clts.elapt[f"Error inserting audit row for {filename} [{db}]: {e}"] = clts.deltat(tstart)


            if db_files:
                # TIDB: REFRESH TABLE only exists in CrateDB
                if db_engine == "crate":
                    cursor.execute("REFRESH TABLE file_audit")


        except Exception as e:
            print(f"Error inserting into {db}: {e}")
            clts.elapt[f"Error inserting into {db}: {e}"] = clts.deltat(tstart)


        finally:
            print(f"Connection closing.... [{db}]")
            if connection is not None:
                connection.close()
                clts.elapt[f"... database connection closed after insert [{db}]"] = clts.deltat(tstart)


    # ================================================================================
    # final execution summary
    # ================================================================================
    print("\n--- FINAL EXECUTION SUMMARY ---")
    print(f"Files found: {files_found_count}")
    print(f"Files downloaded: {files_downloaded_count}")
    print(f"Files skipped by hash: {files_skipped_by_hash_count}")
    print(f"Files sent to parsing: {files_to_process_count}")
    print(f"Files parsed successfully: {files_parsed_count}")
    print(f"Rows inserted @ balcao_digital: {balcao_digital_rows_inserted}")
    print(f"Rows inserted @ balcao_digital_consumo: {balcao_consumo_rows_inserted}")


