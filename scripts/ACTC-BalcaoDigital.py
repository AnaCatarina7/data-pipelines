# helper: build insert values for one table
# ================================================================================
def build_values_to_insert(table, df_insert, existing_ids):
    values_to_insert = []
    skipped = 0
    skipped_list = []


    for _, row in df_insert.iterrows():
        if row["id"] in existing_ids:
            skipped += 1
            skipped_list.append(str(row["tstamp"]))
            continue


        if table == "balcao_digital":
            values_to_insert.append((
                row["id"],
                row["tstamp"].strftime("%Y-%m-%d %H:%M:%S"),
                row["cpe"],
                None if pd.isna(row["potencia_ativa"]) else float(row["potencia_ativa"]),
                None if pd.isna(row["potencia_reativa_ind"]) else float(row["potencia_reativa_ind"]),
                None if pd.isna(row["potencia_reativa_cap"]) else float(row["potencia_reativa_cap"])
            ))
        else:
            values_to_insert.append((
                row["id"],
                row["tstamp"].strftime("%Y-%m-%d %H:%M:%S"),
                row["cpe"],
                None if pd.isna(row["consumo_kw"]) else float(row["consumo_kw"]),
                None if pd.isna(row["estado"]) else str(row["estado"])
            ))


    return values_to_insert, skipped, skipped_list



