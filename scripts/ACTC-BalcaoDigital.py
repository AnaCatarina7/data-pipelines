# helper: open database connection from dblist entry
# ================================================================================
def open_database_connection(user, db):
    dbcreds = get_secret_json(f"{user}-{db}.json")


    if dbcreds.get("dbms") == "crate" or db == "crate":
        from crate import client
        print("... connecting to crate database...")
        connection = client.connect(
            dbcreds["dest_host"],
            username=dbcreds["username"],
            password=dbcreds["password"],
            verify_ssl_cert=True
        )
        cursor = connection.cursor()
        return connection, cursor, "crate", dbcreds


    elif dbcreds.get("dbms") in ("sql", "sql_tls") or db == "tidb":
        import pymysql
        print("... connecting to tidb database...")
        connection = pymysql.connect(
            host=dbcreds.get("host", dbcreds.get("dest_host")),
            port=int(dbcreds.get("port", 4000)),
            user=dbcreds["username"],
            password=dbcreds["password"],
            database=dbcreds["database"],
            autocommit=False,
            charset="utf8mb4",
            cursorclass=pymysql.cursors.Cursor
        )
        cursor = connection.cursor()
        return connection, cursor, "tidb", dbcreds


    else:
        raise ValueError(f"Unsupported DBMS for Balcão Digital: {dbcreds.get('dbms')}")



# ================================================================================
# helper: normalize raw column names
# ================================================================================
def normalize_column_name(value):
    value = "" if value is None else str(value)
    value = value.strip().lower()
    value = unicodedata.normalize("NFKD", value).encode("ascii", "ignore").decode("utf-8")
    value = re.sub(r"[\(\)\[\]\{\}]", " ", value)
    value = re.sub(r"[-_/]", " ", value)
    value = re.sub(r"\s+", " ", value).strip()
    return value



# ================================================================================
# helper: build normalized column map
# ================================================================================
def build_normalized_column_map(columns):
    return {col: normalize_column_name(col) for col in columns}



# ================================================================================
# helper: find first raw column matching one exact normalized candidate
# ================================================================================
def find_first_exact_column(colmap, candidates):
    for raw, norm in colmap.items():
        if norm in candidates:
            return raw
    return None



# ================================================================================
# helper: find first raw column containing one of the normalized fragments
# ================================================================================
def find_first_column_contains(colmap, fragments):
    for raw, norm in colmap.items():
        for fragment in fragments:
            if fragment in norm:
                return raw
    return None



